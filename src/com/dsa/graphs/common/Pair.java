package com.dsa.graphs.common;

public class Pair {
    int element1;
    int element2;

    public Pair(int element1, int element2){
        this.element1 = element1;
        this.element2 = element2;
     }

    public int getElement2() {
        return element2;
    }

    public int getElement1() {
        return element1;
    }
}
